#include "aes_ofb.h"

int main() {
    
    u8 key[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};
    u8 iv[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};

    // Encrypt and decrypt a file
    AES_OFB_Encrypt_File("modes.pdf", "encrypted_ofb.pdf", key, iv);
    
    // Reset IV to the same value as used in encryption
    u8 iv_reset[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};
    memcpy(iv, iv_reset, 16);

    AES_OFB_Decrypt_File("encrypted_ofb.pdf", "decrypted_ofb.pdf", key, iv);

    return 0;
}
