#include "aes_ctr.h"

int main() {
    u8 key[16] = {0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f};
    u8 nonce[16] = {0x7f, 0x2e, 0x1a, 0x4c, 0x3d, 0x8b, 0x92, 0x7e, 0x34, 0xfa, 0x67, 0x89, 0xab, 0xcd, 0xef, 0xf1};


    // Encrypt a PDF file
    AES_CTR_Encrypt_File("modes.pdf", "ctr_encrypted_output.pdf", key, nonce);

    // Decrypt the PDF file
    AES_CTR_Decrypt_File("ctr_encrypted_output.pdf", "ctr_decrypted_output.pdf", key, nonce);

    return 0;
}
